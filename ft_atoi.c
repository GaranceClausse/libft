int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	cpt;

	i = 0;
	nb = 0;
	cpt = 1;
	if (str[0] == '-')
	{
		cpt = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - 48;
		i++;
	}
	return (nb * cpt);
}
