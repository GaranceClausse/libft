int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int	i;

	i = 0;
	if (s1[i] == '\0' || s2[i] == '\0')
		return (0);
	while (i < n && s1[i] != s2[i])
		i++;
	if (i == n - 1)
		return (0);
	else
		return (s1[i] - s2[i]);
}
