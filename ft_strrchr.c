char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i])
		i++;
	while (i >= 0 && str[i] != c)
		i--;
	if (i != 0)
		return (&str[i]);
	else
		return (0);
}
